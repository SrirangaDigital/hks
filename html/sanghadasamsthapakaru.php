<p class="text-justify">ಹಾಸನಜಿಲ್ಲೆಯ, ಕೋರಮಂಗಲದ ಮೂಲದವರಾದ ಶ್ರೀ ವೆಂಕಟರಮಣಯ್ಯ(ಶ್ಯಾನುಭೋಗರು) ಮತ್ತು ಶ್ರೀಮತಿ ನರಸಮ್ಮ ಅವರ ಸುಪುತ್ರಿ ಶ್ರೀಮತಿ ವೆಂಕಟಲಕ್ಷ್ಮಮ್ಮನವರು, ಶ್ರೀ ಹೆಚ್.ಜಿ.ಗಂಗಾಧರ ಶಾಸ್ತ್ರಿಗಳವರ ಧರ್ಮಪತ್ನಿ, ಶ್ರೀಮತಿ ವೆಂಕಟಲಕ್ಷ್ಮಮ್ಮನವರು ಕರಕುಶಲ ಕಲೆಗಳಲ್ಲಿ ಬಹಳ ಆಸಕ್ತಿ ಹೊಂದಿದ್ದರು.</p>
<p class="text-justify">ಶ್ರೀ ಗಂಗಾಧರ ಶಾಸ್ತ್ರಿಗಳು ತಮ್ಮ ನಿರಂತರ ಸೇವೆಯ ನಂತರ, ಸೆಪ್ಟೆಂಬರ್ 19, 1988ರಂದು ನಿಧನರಾದರು.</p>

