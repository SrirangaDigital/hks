<p class="text-justify">ಶ್ರೀಯುತರು ರಂಗಣ್ಣ ಮತ್ತು ವೆಂಕಟಲಕ್ಷ್ಮಮ್ಮ ಅವರ ಪುತ್ರರಾಗಿ 1906ರಲ್ಲಿ ಜನಿಸಿದ ರಾಮಯ್ಯನವರು ಬಾಲ್ಯದಲ್ಲಿ ಬಡತನದಲ್ಲೇ ಬೆಳೆಯುತ್ತಿದ್ದಾಗ ಅವರ ಸೋದರ ಮಾವನಾದ ದಿ|| ಶ್ರೀ.ಜಿ.ವಿ.ಈಶ್ವರಯ್ಯವರ ಕೃಪಾಕಟಾಕ್ಷದಲ್ಲಿ ಮುಂದೆ ಬಂದರು. ಚಿತ್ರಕಲೆಯಲ್ಲಿ ಪರಿಣಿತಿ ಪಡೆದು 30ರ ದಶಕದಲ್ಲಿ ಮೈಸೂರು ಮಹಾರಾಜರ ಅರಮನೆಯ ಚಿತ್ರಗಾರರಾಗಿಯೂ ಸೇವೆ ಸಲ್ಲಿಸಿದರು. ನಂತರ ಶಿವಮೊಗ್ಗದ ಮಂಜಯ್ಯನವರಿಂದ  ಮೈಸೂರು-ಬೆಂಗಳೂರು ಸಂಚರಿಸುತ್ತಿದ್ದ ಬಸ್ಸೊಂದನ್ನು ಖರೀದಿಸಿ ದುಡಿದ ಶ್ರಮದ ಫಲವಾಗಿ ಟಿ.ಎಸ್.ಮೋಟಾರ್ಸ್ ಸಂಸ್ಥೆಯನ್ನೂ, ತದನಂತರ ಶಿವರಾಮಾಪೇಟೆಯಲ್ಲಿ ‘ನಾರಾಯಣ ಅಕ್ಕಿ ಹಿಟ್ಟಿನ ಗಿರಣಿ’ಯನ್ನು ಸ್ಥಾಪಿಸಿದರು. ಇಂದಿನ ಚಾಮುಂಡಿಪುರದಲ್ಲಿರುವ ಶಂಕರನಾರಾಯಣಸ್ವಾಮಿ ದೇವಸ್ಥಾನದಲ್ಲಿ ಕೆಲವು ಗಣ್ಯರ ಸಹಕಾರದಿಂದಾಗಿ ರಾಮೋತ್ಸವದ ವಿಜೃಂಭಣೆಯ ಆಚರಣೆ ಏರ್ಪಾಟು ಕಲಾವಿದರ ಸಂಗೀತ ಕಛೇರಿ.</p>
