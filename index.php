				<h2>ಹೊಯ್ಸಳ ಕರ್ನಾಟಕರು</h2>
			</div>
			
			 <p class="text-justify">ಹೊಯ್ಸಳ ಕರ್ನಾಟಕ ಎಂಬುದು ಸ್ಮಾರ್ತ ಬ್ರಾಹ್ಮಣರಲ್ಲಿ ಪ್ರಸಿದ್ಧವಾದ ಒಂದು ಒಳ ಪಂಗಡದ ಹೆಸರು. ಇವರಲ್ಲಿ ಅನೇಕ ವಿದ್ವಾಂಸರು, ಸಂಗೀತಶಾಸ್ತ್ರಜ್ಞರು ವೇದಾಂತಿಗಳು, ದಂಡನಾಯಕರು ಮತ್ತು 
	 		ಮಠಾಧಿಪತಿಗಳು ಆಗಿಹೋಗಿದ್ದಾರೆ. ಈ ಒಳಪಂಗಡದ ಹೆಸರಿನ ಎರಡು ಪದಗಳಲ್ಲಿ ಕರ್ಣಾಟಕ ಎಂಬ ಎರಡನೆಯ ಪದದ ವಿವರಣೆಯನ್ನು ಸುಲಭವಾಗಿ ನೀಡಬಹುವುದು. ಈ ಗುಂಪಿನ ಮಾತೃಭಾಷೆ ಕನ್ನಡ.</p>
			<p class="text-justify">1908ನೇ ಇಸವಿಯ, ಏಪ್ರಿಲ್ 19 ಮತ್ತು 20ನೇ ತಾರೀಖಿನಂದು ಹೊಯ್ಸಳ ಕರ್ನಾಟಕದ ಭಾಂಧವರೆಲ್ಲರೂ, ಒಂದು ಹೊಸ ಆಲೋಚನೆಗೆ ಜೀವನೀಡಿ, ಬಾಂಧವರನ್ನೆಲ್ಲಾ ಒಂದುಗೂಡಿಸಿ ಸಮಾಜದ ಏಳಿಗೆಗೆ ಶ್ರಮಿಸುವಂತಹ ಒಂದು ಸಂಸ್ಥೆಯನ್ನು ಕಟ್ಟಬೇಕೆಂಬ ಮಹತ್ಕಾರ್ಯಕ್ಕೆ  ತಮ್ಮನ್ನು ತೊಡಗಿಸಿಕೊಂಡರು ಈ ಆಲೋಚನೆ ಮತ್ತು ಕಾರ್ಯರೂಪದ ಪ್ರಭಾವವೇ ಇಂದಿನ ಹೊಯ್ಸಳ ಕರ್ನಾಟಕ ಸಂಘದ (ನೋಂ) ಮೈಸೂರು. ಈ ಎಲ್ಲಾ ಹಿರಿಯರ ಬಗ್ಗೆ ಮತ್ತು ಅವರು ಮಾಡಿದ ಮಹತ್ಕಾರ್ಯಗಳ ಬಗ್ಗೆ ತಿಳಿಸಿಕೊಡುವ ಸಲುವಾಗಿ ಒಂದು ಸಣ್ಣ ಪ್ರಯತ್ನ ನಮ್ಮದು.</p>

</div>
<div class="col-sm-7">
<p class="text-justify">ಹೊಯ್ಸಳ ಕರ್ನಾಟಕ ಸಂಘದ, ಇತಿಹಾಸದ ಆಳದಲ್ಲಿ ತನ್ನ ಬೇರುಗಳು 17ನೇ ಶತಮಾನದಿಂದ ದೊರೆಯುತ್ತದೆ. ಸುಮಾರು 17ನೇ ಶತಮಾನದ ಅಂತ್ಯದಲ್ಲಿ ನಮ್ಮ ಪೂರ್ವಜರು ಒಂದುಗೂಡುವ ಸಲುವಾಗಿ ಹಲವಾರು ಸಭೆ, ಸಮಾರಂಭವನ್ನು ನಡೆಸುತ್ತಿದ್ದರು. ನಂತರ 1890ರಲ್ಲಿ ಪ್ಲೇಗ್ನದ ಉಪದ್ರವ ಪ್ರಾಪ್ತವಾದ ಕಾರಣ ಜನಗಳು ಗುಂಪುಸೇರುವುದರಿಂದ ಊರಿನಲ್ಲಿ, ರೋಗವು ಹರಡಿಕೊಳ್ಳಬಹುದೆಂಬ ಶಂಕೆಯಿಂದಲೂ ಮತ್ತು ನಮ್ಮಲ್ಲಿ ಅನೇಕರು ರೋಗಭಿತಿಯಿಂದ ಪರಸ್ಥಳಗಳಿಗೆ ಹೋಗುತ್ತಾ ಇದ್ದುದರಿಂದಲೂ ಕೆಲವು ಕಾಲ ಸಭೆಯು ಯಥಾಕ್ರಮವಾಗಿ ನಡೆಯಲು ಅವಕಾಶವಾಗಲಿಲ್ಲ.</p>
<p class="text-justify">ಆದರೆ 1891ನೇ ಇಸವಿಯ, ಮೈಸೂರು ದೇಶದ ಸೆನ್ಸಗಸ್ ದಾಖಲೆ ಪ್ರಕಾರ ಬೆಂಗಳೂರು ಡಿಸ್ಟ್ರಿಕ್ಟ್ನರಲ್ಲಿ 923, ಕೋಲಾರ 79, ತುಮಕೂರು 1466, ಮೈಸೂರು 2866, ಹಾಸನ 1867, ಶಿವಮೊಗ್ಗ 122, ಚಿಕ್ಕಮಂಗಳೂರು 982, ಚಿತ್ರದುರ್ಗ 23, ಈ ರೀತಿಯಾಗಿ ಮೈಸೂರು ಸಂಸ್ಥಾನದಲ್ಲಿ, ನಮ್ಮ ಜನರು ಒಟ್ಟು 8328 ಮಂದಿ ಇರುವುದಾಗಿಯೂ ಈ ಸಂಖ್ಯೆಯಲ್ಲಿ  4121 ಮಂದಿ ಗಂಡಸರು, ಮತ್ತು 4207 ಮಂದಿ ಹೆಂಗಸರು ಇರುವಂತೆಯೂ ತಿಳಿಯಬರುತ್ತದೆ. ಇದಲ್ಲದೆ ದಕ್ಷಿಣ ಕನ್ನಡ ಜಿಲ್ಲಾದಲ್ಲಿ ಸುಮಾರು 400 ಜನಗಳು, ಕುಂಭಕೋಣ ಪ್ರಾಂತ್ಯದಲ್ಲಿ ಸುಮಾರು 600 ಜನಗಳು ಮತ್ತು ಕಾಶಿ ಮೊದಲಾದ ಕ್ಷೇತ್ರಗಳಲ್ಲಿ ಅಲ್ಪಸ್ವಲ್ಪ ಜನಗಳು ಸಹ ಇರುವುದಾಗಿಯೂ ತಿಳಿದುಬರುತ್ತದೆ.</p>
<p class="text-justify">ಇದಾದ ನಂತರ ನಮ್ಮೊಳಗೆ ಸಂಘವನ್ನು ಸ್ಥಾಪಿಸುವ ಅವಶ್ಯಕತೆ ಇದೆಯೇ? ಎಂದು  ಚರ್ಚಿಸಿದ ನಮ್ಮ ಹಿರಿಯರು ಅಂದಿನ ದೇಶದ ಕಾಲಸ್ಥಿತಿ, ಹಿಂದಿಗಿಂತಲೂ ವತ್ಯಾಯಗಳಾಗಿದ್ದು, ದೇಶಿಯರಿಗೆ ಪಾಶ್ಚಿಮಾತ್ಯರ ಸಂಗವುಂಟಾದುದರಿಂದ ಆಚಾರ ವ್ಯವಹಾರಾದಿಗಳಲ್ಲಿ ಕೆಲವು ಬದಲಾವಣೆಯಾಗುತ್ತಿರುವುದು ಕಂಡುಬರತೊಡಗಿತು. ನಾಗರೀಕತೆ ಎಂಬ ಪ್ರವಾಹವು ಉಕ್ಕುತ್ತಿರುವ ಈ ಸಂಧರ್ಭದಲ್ಲಿ ಸುಮುದಾಯದ ಬಾಂಧವರ ವಿದ್ಯಾಸಾಧನೆಗೆ, ಅದಕ್ಕೆ ಅನುಕೂಲ ಕಲ್ಪಿಸುವುದಕ್ಕೆ ಹಾಗೂ ಪಂಗಡದವರ ಪುರೋಭಿವೃದ್ಧಿಗೆ, ಸಂಘದ ಸ್ಥಾಪನೆಯ ಅವಶ್ಯಕತೆ ಇರುವುದನ್ನು ಕಂಡುಬಂದರು.</p>
