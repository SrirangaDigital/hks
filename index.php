<p class="text-justify">ಈ ಕಾರಣಗಳಿಂದಾಗಿ, 1908ನೇ ಇಸವಿಯಲ್ಲಿ, ಒಂದು ಉಪಸಭೆ ಸೇರಿದ ಮೈಸೂರಿನ ಅಗ್ರಗಣ್ಯರು, ಸಂಘದ ಸ್ಥಾಪನೆಗಾಗಿ ಏಕದೇಶವಾಗಿ ಮೈಸೂರಿನವರೇ ಪ್ರಾರಂಭಿಸುವುದಕ್ಕಿಂತಲೂ ಎಲ್ಲಾಕಡೆಗಳಿಂದಲೂ ಮುಖಂಡರಾಗಿರತಕ್ಕ ನಮ್ಮವರನ್ನು ಬರಮಾಡಿಕೊಂಡ ಸರ್ವ ಸಮ್ಮತವಾಗಿ ಈ ಕೆಲಸವನ್ನು ನಡೆಸುವುದು ಸೂಕ್ತವೆಂದು ಭಾವಿಸಿದರು, ಆ ಕಾರಣ ಒಂದು ಮುಖ್ಯಸಭೆಯನ್ನು ನಡೆಸುವುದಾಗಿಯೂ ಮತ್ತು ಆ ಸಭೆಯನ್ನು “ಹೊಯ್ಸಳ ಕರ್ನಾಟಕ ಮಂಡಲದ ಮಹಾ ಸಭೆ” ಎಂದು ಕರೆಯುವುದಾಗಿಯೂ ನಿರ್ಣಯಿಸಿ, ಅದೇ ಇಸವಿಯ ಅಂದರೆ, ಕೀಲಕ ಸಂವತ್ಸರದ ಚೈತ್ರ ಬಹಳ 2ನೇ ಭಾನುವಾರಕ್ಕೆ ಸರಿಯಾದ 19<i>-</i>04<i>-</i>1908ನೆಯ ತಾರೀಖೀನ ಮಧ್ಯಾಹ್ನ 2 ಘಂಟೆಯ ವೇಳೆಗೆ ಮೈಸೂರು ಹಳೇ ಅಗ್ರಹಾರದಲ್ಲಿರುವ ಸರದಾರ ಕಾಂತರಾಜ ಅರಸಿನವರ ತೊಟ್ಟಿಯಲ್ಲಿ ಸೇರುವುದಾಗಿಯೂ ನಿರ್ಣಯಿಸಿತು.</p>


